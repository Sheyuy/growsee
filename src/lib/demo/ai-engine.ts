function matchKeyword(text: string): number {
  const lower = text.toLowerCase();
  for (let i = 0; i < KNOWLEDGE_BASE.length; i++) {
    for (const kw of KNOWLEDGE_BASE[i].keywords) {
      if (lower.includes(kw.toLowerCase())) return i;
    }
  }
  return -1;
}

export function generateAIReply(message: string, history: { role: "user" | "assistant"; content: string }[]): AIResponse {
  const idx = matchKeyword(message);

  if (idx >= 0) {
    const kb = KNOWLEDGE_BASE[idx];
    const response = kb.responses[Math.floor(Math.random() * kb.responses.length)];
    return {
      content: response,
      autoSaved: true,
      followUp: kb.followUp,
    };
  }

  // 如果没有关键词匹配，返回随机兜底回复
  const fallback = FALLBACK_RESPONSES[Math.floor(Math.random() * FALLBACK_RESPONSES.length)];
  return {
    content: fallback,
    autoSaved: false,
  };
}

/* 生成流式响应：逐字返回 */
export async function* streamAIReply(message: string, history: { role: "user" | "assistant"; content: string }[]): AsyncGenerator<string> {
  const reply = generateAIReply(message, history);
  const content = reply.content;
  const chunkSize = 3; // 每次返回3个字符，模拟打字效果

  for (let i = 0; i < content.length; i += chunkSize) {
    yield content.slice(i, i + chunkSize);
    await new Promise((r) => setTimeout(r, 30)); // 30ms 间隔，模拟打字
  }
}
